/*
    En esta clase veremos los metodos de los string, ya que estos
    se pueden ver como un objeto que tiene sus propios metodos
    Trabajemos con la siguiente variable:
*/ 

const producto="Monitor de 20 pulgadas";

console.log(producto);

/*
    Si que remos saber el largo de la cadena de texto utilizamos
    el metodo length.
*/ 

console.log(producto.length);

/*
    Tambien hay meotodos para comprobar si la cadena contiene
    sienta secuencia de caracteres, por ejemplo el metodo
    includes
*/ 

console.log(producto.includes("Tablet"));