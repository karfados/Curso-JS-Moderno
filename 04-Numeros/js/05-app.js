/*
    En esta seccion veremos como incrementar o decrementar valores
*/

let puntaje=5;

//Evalua y luego suma
puntaje++;
//Suma y luego evalua
++puntaje;

//Evalua y luego Resta
puntaje--;
//Resta y luego evalua
--puntaje;

// Tambien se puede incrementar o decrementar en cantidades mas grandes

puntaje+=3;
puntaje-=5;

console.log(puntaje)