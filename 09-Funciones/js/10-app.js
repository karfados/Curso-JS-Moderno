/**
 * En esta seccion veremos que son los arrow Funcions.
 *
 * Esta es otra forma de declarar funciones mas simplificada, ademas de
 * eso son ampliamente utilizadas en frameworks actuales.
 *
 * La sintasix para utilizar una arrow funcion es la siguiente:
 */

const suma = () => {};

/**
 * La ventaja de las arrow funcions es que se pueden simplificar
 * mucho, por ejemplo:
 * si no tiene parametros lleva parentesis o si tiene mas de un parametro
 * y si solo toene una linea la guncion lleva implisita el return y
 * ademas no son nesesarioas las llaves.
 *
 * Veamos el siguiente ejemplo:
 */

const aprendo = () => "Estoy aprendiendo";
console.log(aprendo());
