/**
 * En esta seccion veremos que es un parametro y un argumento.
 * 
 * Los argumentos son las variables que definimos en la declaracion
 * de la funcion para ser usados cuando la instanciemos, mientras que
 * los parametos son los datos que le pasamos a la funcion cuando la 
 * instanciamos.
 */

function suma(num1,num2){ //Los Argumentos de la funcion estan dentro de los parentesis
    return num1+num2;
}

