/**
 * En esta seccion veremos como trabajar con arreglos(Arrays).
 *
 * Los arrays se utilizan cuando almacenamos múltiples valores
 * en una sola variable, mientras que un objeto puede contener
 * múltiples variables con sus valores.
 */

//Para declarar un arreglo lo hacemos de la siguiente forma:
let numeros = [1, 2, 3, 4, 5];
// recordemos que los corchetes se usan para declarar arreglos
//Ahora veremos que un arreglo puede contener multiples datos
const DETODO = [1,"a",{name:"Carlos",edad:15},[1,2,3,4],true];

// Otra forma de declarar arreglos es por metodo new Array:
const meses = new Array("enero","febrero")

console.log(numeros);
console.log(DETODO);
console.log(meses)
